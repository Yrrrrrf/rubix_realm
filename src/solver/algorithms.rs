//! Algorithms for solving the Rubik's Cube.
//! 
//! This file implements the CFOP method (or Fridrich Method (by Jessica Fridrich)) for solving the Rubik's Cube.
//! - Cross 
//! - First Two Layers (F2L)
//! - Orientation of the Last Layer (OLL)
//! - Permutation of the Last Layer (PLL)
//! 
#![allow(unused)]

// Cross
pub fn cross() {
    println!("Cross");
}

// First Two Layers
pub fn f2l() {
    println!("F2L");
}

// Orientation of the Last Layer
pub fn oll() {
    println!("OLL");
}

// Permutation of the Last Layer
pub fn pll() {
    println!("PLL");
}

// Fridrich Method
pub fn fridrich() {
    println!("Fridrich Method");

    cross();
    f2l();
    oll();
    pll();
}
